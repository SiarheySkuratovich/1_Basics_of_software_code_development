public class Dot {
    private int x;
    private int y;

    Dot() {

    }

    Dot(int usersX, int usersY) {
        x = usersX;
        y = usersY;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
